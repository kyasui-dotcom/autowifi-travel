function agreeOnly(extras = {}) {
  return {
    agreeOnly: {
      actions: [
        ...(extras.preCheck ? [{
          description: 'Accept terms checkbox',
          selector: "input[type='checkbox']",
          fallbackSelectors: ['.terms-checkbox', '#agree', '.checkbox'],
          action: 'check',
          delayMs: 500,
        }] : []),
        {
          description: 'Click connect button',
          selector: "button[type='submit'], .btn-connect, .submit-btn, input[type='submit']",
          fallbackSelectors: ['button:last-of-type', 'a.btn', '.btn-primary'],
          action: 'click',
          delayMs: 500,
        },
      ],
      successCondition: {
        method: 'http_probe',
        value: 'http://connectivitycheck.gstatic.com/generate_204',
      },
    },
  };
}

function registration() {
  return {
    registration: {
      fields: [
        {
          fieldId: 'email',
          selector: "input[name='email'], input[type='email']",
          fallbackSelectors: ["input[type='text']:first-of-type"],
          valueSource: 'profile.email',
          inputMethod: 'set_value',
          delayMs: 500,
        },
      ],
      postFillActions: [
        {
          description: 'Accept terms',
          selector: "input[type='checkbox'], .terms-agree",
          fallbackSelectors: ['#agree', '.checkbox'],
          action: 'check',
          delayMs: 300,
        },
        {
          description: 'Submit',
          selector: "button[type='submit'], .connect-btn, input[type='submit']",
          fallbackSelectors: ['button:last-of-type', '.btn-primary'],
          action: 'click',
          delayMs: 500,
        },
      ],
      successCondition: {
        method: 'http_probe',
        value: 'http://connectivitycheck.gstatic.com/generate_204',
      },
    },
  };
}

